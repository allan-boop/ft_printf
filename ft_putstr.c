#include "ft_printf.h"

void	ft_putstr(char *str, t_flags *ret)
{
	if (!str)
	{
		write(1, "(null)", 6);
		ret->len += 6;
		return ;
	}
	while (*str)
	{
		write(1, str, 1);
		str++;
		ret->len++;
	}
}
